    output logic [31:0] halt_instr,
    output logic [3:0]  halt_reason,
    output logic        idle,
    output logic [31:0] debug_arch_pc,
    output logic [31:0] debug_arch_gpr
);
    logic program_ready;
    // The 47 accepted Profile mechanisms are explicit here rather than being
    // inferred from a private Makefile.  All Oracle/reachability parameters
    // retain their disabled defaults.
    ooo_npc_top #(
        .USE_PMEM_IFETCH(1'b1),
        .ALU_LATENCY(1), .ALU1_LATENCY(1),
        .RAW_COMPLETION_WAKEUP_ENABLE(1'b1),
        .SAME_CYCLE_DEPENDENCY_FORWARDING_ENABLE(1'b1),
