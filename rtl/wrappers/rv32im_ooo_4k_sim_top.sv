        .STORE_ISSUE_FINAL_FALLTHROUGH_ENABLE(1'b1),
        .MDU_MUL_COMPLETION_FALLTHROUGH_ENABLE(1'b1),
        .POSTED_STORE_RESPONSE_OWNERSHIP_ENABLE(1'b1),
        .POSTED_STORE_RESPONSE_PIPELINE_ENABLE(1'b1),
        .DUAL_POSTED_STORE_RESPONSE_OWNER_ENABLE(1'b1),
        .LOAD_TRANSACTION_DEPTH3_ENABLE(1'b1),
        .IFETCH_SAME_EDGE_RESPONSE_CAPTURE_ENABLE(1'b1),
