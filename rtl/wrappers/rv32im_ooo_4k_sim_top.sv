        .LOAD_RESPONSE_COMPLETION_BYPASS_ENABLE(1'b1),
        .LSU_REQUEST_FALLTHROUGH_ENABLE(1'b1),
        .PAIR_RAW_TO_FINAL_BYPASS_ENABLE(1'b1),
        .SERIAL_ORDINARY_COMPLETION_CONCURRENCY_ENABLE(1'b1),
